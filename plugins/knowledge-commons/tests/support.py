#!/usr/bin/env python3
"""Test support: materialize a fixture graph, mutate it, and run the validator on it.

Broken fixtures are generated by MUTATION, never checked in as directories. Forty
near-identical broken graphs rot within a month, and the diff between the valid one and the
broken one -- which is the only interesting part -- would be invisible in the tree.
"""

import os
import shutil
import sys
import tempfile
import unittest

HERE = os.path.dirname(os.path.abspath(__file__))
BIN = os.path.join(os.path.dirname(HERE), "bin")
FIXTURES = os.path.join(HERE, "fixtures")

sys.path.insert(0, BIN)

import validate  # noqa: E402
import _miniyaml  # noqa: E402


def fixture(name):
    """Absolute path to a pristine fixture graph."""
    return os.path.join(FIXTURES, name)


def materialize(base="orchard", edits=None, deletes=None, adds=None):
    """Copy a fixture into a temp dir and apply mutations. Returns the new graph root.

    `edits` maps a relative path to a callable(text) -> text.
    """
    root = tempfile.mkdtemp(prefix="kc-fixture-")
    dest = os.path.join(root, base)
    shutil.copytree(fixture(base), dest)
    for rel in deletes or []:
        os.remove(os.path.join(dest, rel))
    for rel, text in (adds or {}).items():
        path = os.path.join(dest, rel)
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w", encoding="utf-8") as fh:
            fh.write(text)
    for rel, mutate in (edits or {}).items():
        path = os.path.join(dest, rel)
        with open(path, "r", encoding="utf-8") as fh:
            text = fh.read()
        with open(path, "w", encoding="utf-8") as fh:
            fh.write(mutate(text))
    return dest


def run_check(root, scope=None, source_scan=False):
    """Run the validator in-process. Returns a list of Finding."""
    config = validate.load_config(root)
    findings = validate.check_config(config)
    if not any(f.severity == "error" for f in findings):
        graph = validate.build_graph(root, config)
        findings.extend(validate.run_graph_checks(graph, source_scan=source_scan))
    if scope:
        wanted = set(scope)
        findings = [f for f in findings if wanted & set(f.paths)]
    return findings


def codes(findings, severity=None):
    """The set of check codes present, optionally filtered by severity."""
    return {f.check for f in findings if severity is None or f.severity == severity}


class GraphCase(unittest.TestCase):
    """Base class carrying the assertions every check test uses."""

    #: Every check code asserted by any test in this suite. The meta-test reads it.
    asserted = set()

    def assertFires(self, code, findings, severity="error"):
        """Assert `code` fired at `severity`, and say what DID fire when it did not."""
        GraphCase.asserted.add(code)
        hits = [f for f in findings if f.check == code]
        self.assertTrue(
            hits,
            "%s did not fire. Findings were: %s"
            % (code, sorted(codes(findings)) or "none"))
        self.assertIn(
            severity, {f.severity for f in hits},
            "%s fired, but not at severity %r (got %r)"
            % (code, severity, sorted({f.severity for f in hits})))

    def assertClean(self, findings):
        """Assert no errors at all."""
        errors = [f for f in findings if f.severity == "error"]
        self.assertEqual(
            [], errors,
            "expected no errors, got: %s"
            % [(f.check, f.locus(), f.message) for f in errors])
